--DISPLAY FIRST NAME AND LASTNAME AND DEPARTMENT NAME CITY FOR ALL EMPLOYEES
SELECT FIRST_NAME,LAST_NAME,DEPARTMENT_NAME,CITY FROM EMPLOYEES E
JOIN DEPARTMENTS D ON E.DEPARTMENT_ID=D.DEPARTMENT_ID JOIN LOCATIONS L
ON D.LOCATION_ID=L.LOCATION_ID;

--DISPLAY FIRST NAME AND LASTNAME AND DEPARTMENT NAME CITY AND COUNTRY_NAME FOR ALL EMPLOYEES
SELECT FIRST_NAME,LAST_NAME,DEPARTMENT_NAME,CITY,COUNTRY_NAME,REGION_NAME FROM EMPLOYEES E
JOIN DEPARTMENTS D ON E.DEPARTMENT_ID=D.DEPARTMENT_ID
JOIN LOCATIONS L ON D.LOCATION_ID=L.LOCATION_ID
JOIN COUNTRIES C ON L.COUNTRY_ID = C.COUNTRY_ID
JOIN REGIONS R ON R.REGION_ID=C.REGION_ID;

--HOW MANY EMPLOYEES WORKING FOR EACH COUNTRY_NAME
SELECT COUNTRY_NAME,COUNT(*) FROM EMPLOYEES E
JOIN DEPARTMENTS D ON E.DEPARTMENT_ID=D.DEPARTMENT_ID
JOIN LOCATIONS L ON D.LOCATION_ID=L.LOCATION_ID
JOIN COUNTRIES C ON L.COUNTRY_ID = C.COUNTRY_ID
GROUP BY COUNTRY_NAME
HAVING COUNT(*)>10;

