-- DISPLAY ALL FIRST_NAME AND RELATED DEPARTMENTS NAME

SELECT FIRST_NAME,DEPARTMENT_NAME FROM EMPLOYEES E
JOIN DEPARTMENTS D ON E.DEPARTMENT_ID=D.DEPARTMENT_ID;

--ORDER THE LIST BASED ON FIRSTNAME ASC
SELECT FIRST_NAME,DEPARTMENT_NAME FROM EMPLOYEES E
JOIN DEPARTMENTS D ON E.DEPARTMENT_ID=D.DEPARTMENT_ID
ORDER BY FIRST_NAME;

--display all first_name and department name including the employee without department

SELECT FIRST_NAME,DEPARTMENT_NAME FROM EMPLOYEES E
LEFT OUTER JOIN DEPARTMENTS D ON E.DEPARTMENT_ID=D.DEPARTMENT_ID
ORDER BY FIRST_NAME;

--3DISPLAY ALL FIRST_NAME AND DEPARTMENT_NAME INCLUDING THE DEPARTMENT WITHOUT EMPLOYEE
SELECT FIRST_NAME,DEPARTMENT_NAME FROM EMPLOYEES E
RIGHT JOIN DEPARTMENTS D ON E.DEPARTMENT_ID=D.DEPARTMENT_ID;

--HOW MANY EMPLOYEE WE HAVE FOR EACH DEPARTMENT NAME
SELECT DEPARTMENT_NAME,COUNT(*) FROM EMPLOYEES E
RIGHT JOIN DEPARTMENTS D ON E.DEPARTMENT_ID=D.DEPARTMENT_ID
GROUP BY DEPARTMENT_NAME;

